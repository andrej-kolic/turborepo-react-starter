import { interactionToLocator } from './locator.js';

function hrefForCompare(raw) {
  try {
    return new URL(raw).href;
  } catch {
    return raw;
  }
}

function pushGoto(actionLines, state, targetUrl) {
  const href = hrefForCompare(targetUrl);
  if (state.lastGoto === href) return;
  state.lastGoto = href;
  actionLines.push(`  await page.goto(${JSON.stringify(targetUrl)});`);
}

export function generatePlaywrightTest(url, interactions) {
  const actionLines = [];
  const navState = { lastGoto: null };

  pushGoto(actionLines, navState, url);

  for (const interaction of interactions) {
    const locator = interactionToLocator(interaction);
    let comment = '';
    const r = interaction.react;
    if (r) {
      const src = r.originalSource
        ? `${r.originalSource.source}:${r.originalSource.line}`
        : r.fileName
          ? `${r.fileName}:${r.lineNumber}`
          : null;
      const name = r.componentName
        ? r.componentName.replace(/\*\//g, '')
        : null;
      if (name || src) comment = ` // ${[name, src].filter(Boolean).join(' ')}`;
    }

    switch (interaction.type) {
      case 'navigate':
        pushGoto(actionLines, navState, interaction.url);
        break;
      case 'click':
        if (!locator) break;
        actionLines.push(`  await ${locator}.click();${comment}`);
        break;
      case 'fill':
        if (!locator) break;
        actionLines.push(
          `  await ${locator}.fill(${JSON.stringify(interaction.value ?? '')});${comment}`,
        );
        break;
      case 'check':
        if (!locator) break;
        actionLines.push(`  await ${locator}.check();${comment}`);
        break;
      case 'uncheck':
        if (!locator) break;
        actionLines.push(`  await ${locator}.uncheck();${comment}`);
        break;
      case 'selectOption':
        if (!locator) break;
        actionLines.push(
          `  await ${locator}.selectOption(${JSON.stringify(interaction.value ?? '')});${comment}`,
        );
        break;
      default:
        break;
    }
  }

  let testLabel;
  try {
    const u = new URL(url);
    testLabel = u.hostname + u.pathname;
  } catch {
    testLabel = url;
  }

  return [
    `import { test, expect } from '@playwright/test';`,
    ``,
    `// Generated by browser-capture record-interactions`,
    `// Source: ${url}`,
    `// Captured: ${new Date().toISOString()}`,
    ``,
    `test(${JSON.stringify('recorded: ' + testLabel)}, async ({ page }) => {`,
    ...actionLines,
    `});`,
    ``,
  ].join('\n');
}
